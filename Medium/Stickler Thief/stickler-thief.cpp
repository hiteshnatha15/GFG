class Solution
{
    public:
    //Function to find the maximum money the thief can get.
    int FindMaxSum(int arr[], int n)
    {
        // Your code here
        int x=0;
        int y=0;
        for(int i =0;i<n;i++){
            if(i%2==0){
             x = max(x + arr[i], y);
        } else {
            y = max(y + arr[i], x);
        }
        }
        return max(x,y);
    }
};

