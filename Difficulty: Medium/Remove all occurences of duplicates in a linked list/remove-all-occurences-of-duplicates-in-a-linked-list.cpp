/* Linked List node structure

struct Node {
    int data;
    struct Node *next;
};

*/

class Solution {
  public:
    Node* removeAllDuplicates(struct Node* head) {
        // code here
        Node* node=new Node(-1);
        Node* cur=node;
        if(head==NULL) return NULL;
        if(head->next==NULL) return head;
        
        int prev=-1;
        while(head->next!=NULL){
            if(head->next->data!=head->data && head->data!=prev){
                cur->next=new Node(head->data);
                cur=cur->next;
            }
            prev=head->data;
            head=head->next;
        }
        if(prev!=head->data){
            cur->next=new Node(head->data);
        }
        return node->next;
    }
};

